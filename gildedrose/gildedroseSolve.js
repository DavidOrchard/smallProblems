describe('Test suite', function() {
  const tests = [
      {args: ["foo", 1, 1],expected:[{name: "foo", sellIn: 0, quality: 0}]}
  ];
  tests.forEach(({args, expected}) => 
      it('check no items', function () {
        const unknown = new Item(...args);
        const shop = new Shop([unknown]);
        shop.updateQuality();
        assert.deepEqual(shop.items[0], expected[0]);
      })
  );
});

mocha.run();